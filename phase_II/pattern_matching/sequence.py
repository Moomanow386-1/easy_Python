# sequence pattern คือ การจับคู่กับลำดับของค่าหรือองค์ประกอบในกรณีที่ต้องการจับคู่กับลำดับของข้อมูลที่มีความสัมพันธ์กันหรือมีความคล้ายคลึงกัน เช่น การจับคู่กับลิสต์ ทูเพิล หรือสตริง เป็นต้น

data = []
match data:
    case []:
        print("ข้อมูลว่าง")
    case [1,2,3]:
        print("ข้อมูลคือ [1,2,3]")
    case [1,2,3,4,5]:
        print("ข้อมูลคือ [1,2,3,4,5]")    
    case _:
        print("ข้อมูลไม่ถูกต้อง")