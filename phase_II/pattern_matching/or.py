#or pattern คือ การใช้เครื่องหมาย | เพื่อจับคู่กับค่าหลายๆ ค่าในกรณีที่ต้องการจับคู่กับค่าที่มีความสัมพันธ์กันหรือมีความคล้ายคลึงกัน
data = input("ป้อนคำนำหน้าชื่อ: ")
match data:
    case "นาย" | "นาง" | "นางสาว": 
        print("คุณเป็นผู้ชายหรือผู้หญิง")
    case _:
        print("ข้อมูลไม่ถูกต้อง")