# guard pattern คือ การใช้เงื่อนไขเพิ่มเติม (if)ในการจับคู่กับค่าในกรณีที่ต้องการตรวจสอบเงื่อนไขเพิ่มเติมเพื่อให้การจับคู่นั้นสำเร็จหรือไม่
score = int(input("กรุณาใส่คะแนนของคุณ (0-100): "))
print("คะแนนของคุณคือ:", score)
match score:
    case 100:
        print("สอบได้คะแนนเต็ม")
    case score if 99 >= score >= 50:
        print("สอบผ่าน")
    case _:
        print("สอบไม่ผ่าน")