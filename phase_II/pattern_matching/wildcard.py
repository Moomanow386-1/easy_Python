# Wildcard Pattern คือ การใช้เครื่องหมาย _ เพื่อจับคู่กับค่าที่ไม่สนใจหรือไม่ต้องการใช้งานในกรณีที่มีหลายกรณีที่ต้องการจับคู่กับค่าเดียวกัน
service = 100
match service:
    case 1:
        print("ฝากเงิน")
    case 2:    
        print("ถอนเงิน")
    case 3:
        print("สอบถามยอดคงเหลือ")
    case _:
        print("ข้อมูลไม่ถูกต้อง")