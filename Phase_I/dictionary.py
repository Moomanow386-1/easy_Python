color = {"red": "แดง", 
         "green": "เขียว",
         "blue": "น้ำเงิน"
        }
print(color.keys())    #การใช้เมธอด keys() เพื่อดึงเฉพาะคีย์ทั้งหมดใน dictionary ออกมาเป็นวัตถุชนิด dict_keys ซึ่งสามารถแปลงเป็น list ได้ถ้าต้องการ
print(color.values())  #การใช้เมธอด values() เพื่อดึงเฉพาะค่าทั้งหมดใน dictionary ออกมาเป็นวัตถุชนิด dict_values ซึ่งสามารถแปลงเป็น list ได้ถ้าต้องการ
print(color.items())   #การใช้เมธอด items() เพื่อดึงคู่คีย์-ค่า ทั้งหมดใน dictionary ออกมาเป็นวัตถุชนิด dict_items ซึ่งสามารถแปลงเป็น list ได้ถ้าต้องการ
print(color.get("red"))    #การใช้เมธอด get() เพื่อเข้าถึงค่าของคีย์ใน dictionary โดยการระบุชื่อคีย์เป็นอาร์กิวเมนต์ ซึ่งจะคืนค่าที่เกี่ยวข้องกับคีย์นั้น หรือคืนค่า None ถ้าคีย์ไม่พบใน dictionary
print(color.get("yellow"))    #การใช้เมธอด get() พร้อมกับการกำหนดค่าเริ่มต้น (default value) เพื่อให้คืนค่าที่กำหนดไว้แทนค่า None เมื่อคีย์ที่ระบุไม่พบใน dictionary

for i in color.keys():    #การใช้ลูป for เพื่อวนซ้ำผ่านคีย์ทั้งหมดใน dictionary โดยใช้เมธอด keys() เพื่อดึงคีย์ออกมา
    print(i)

for key,value in color.items():    #การใช้ลูป for เพื่อวนซ้ำผ่านคู่คีย์-ค่า ทั้งหมดใน dictionary โดยใช้เมธอด items() เพื่อดึงคู่คีย์-ค่า ออกมาเป็นตัวแปร key และ value ในแต่ละรอบของลูป
    print(key,"=",value)

color.pop("red")   #การใช้เมธอด pop() เพื่อดึงค่าของคีย์ที่ระบุออกจาก dictionary และลบคีย์นั้นออกจาก dictionary ด้วย โดยจะคืนค่าที่เกี่ยวข้องกับคีย์ที่ถูกดึงออกมา
print(color)

color.clear()    #การใช้เมธอด clear() เพื่อทำให้ dictionary ว่างเปล่า โดยการลบคู่คีย์-ค่า ทั้งหมดออกจาก dictionary
print(color)

color.update({"yellow": "เหลือง", "pink": "ชมพู"})    #การใช้เมธอด update() เพื่อเพิ่มคู่คีย์-ค่า ใหม่ลงใน dictionary โดยการระบุเป็น dictionary อื่นที่มีคู่คีย์-ค่า ที่ต้องการเพิ่มเข้ามา
print(color)
color.update({"yellow": "เหลืองเข้ม"})   
print(color)

maincolor = color.copy()    #การใช้เมธอด copy() เพื่อสร้างสำเนาของ dictionary ซึ่งจะคืนค่าเป็น dictionary ใหม่ที่มีคู่คีย์-ค่า เหมือนกับ dictionary ต้นฉบับ แต่เป็นวัตถุคนละตัวกัน
print(maincolor)