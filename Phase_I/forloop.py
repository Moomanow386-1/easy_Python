#for loop คือ การทำซ้ำที่กำหนดจำนวนครั้งที่แน่นอน โดยใช้ตัวแปรในการเก็บค่าที่เปลี่ยนแปลงในแต่ละรอบของการทำซ้ำ
#while loop คือ การทำซ้ำที่ไม่กำหนดจำนวนครั้งที่แน่นอน โดยจะทำซ้ำจนกว่าจะมีเงื่อนไขที่กำหนดไว้เป็นเท็จ

for counter in range(3): #เริ่มต้นที่ 0-2
    print(counter)

for i in range(5,11): #เริ่มต้นที่ 5-10
    print(i)

for j in range(1,11,2): #เริ่มต้นที่ 1-10 แต่ละรอบเพิ่มขึ้น 2
    print(j)

for k in range(10,0,-2): #เริ่มต้นที่ 10-0 แต่ละรอบลดลง 2
    print(k)