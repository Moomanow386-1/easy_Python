num1 = 5
num2 = 2
result = num1 // num2     #การหารปัดเศษลง (floor division) จะได้ผลลัพธ์เป็นจำนวนเต็มเสมอ แม้จะมีเศษก็ตาม
rising = num1 ** num2     #การยกกำลัง (exponentiation) 
percent = num1 % num2     #การหารเอาเศษ (modulo) จะได้ผลลัพธ์เป็นเศษที่เหลือจากการหาร
print(result)
print(rising)
print(percent)